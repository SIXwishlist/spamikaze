<h1>Spamikaze Documentation</h1>

Spamikaze is an automated spam block system.  Unlike some other spam
block systems, Spamikaze does no tests for open proxy or open relay
vulnerabilities.  Spamikaze is passive and completely driven by mail
coming into a system's spamtrap addresses.

